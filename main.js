// First FAQ

function faqOpen(){
    document.getElementById("faq-first-answer").style.display="block";
    document.getElementById("plus-symbol").style.display="none";
    document.getElementById("close-symbol").style.display="block";
}
function faqClose(){
    document.getElementById("close-symbol").style.display="none";
    document.getElementById("faq-first-answer").style.display="none";
    document.getElementById("plus-symbol").style.display="block";
}

